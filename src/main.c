#include "../include/minishell.h"

int	main(int argc, char *argv[], char *envp[])
{
	t_data	mish;

	if (argc != 1)
	{
		printf("minishell takes no arguments, you passed %s\n", argv[1]);
		exit(ERROR);
	}
	init_mish(&mish, envp);
	init_prompt(&mish);
	return (0);
}
